package com.example.mohit.recyclerviewdemo15;

public class SingleRow {
    String name, desig;
    int image;

    public SingleRow(String name, String desig, int image) {
        this.name = name;
        this.desig = desig;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getDesig() {
        return desig;
    }

    public int getImage() {
        return image;
    }
}
